# Day 1 - Example 5: Calculator Fun
# Python is a super-fast calculator!

# When there are NO quotes, Python does the math for you
print(2 + 3)      # addition -> 5
print(10 - 4)     # subtraction -> 6
print(6 * 7)      # multiplication (the * symbol) -> 42
print(20 / 5)     # division (the / symbol) -> 4.0

# You can mix a text label with the answer using a comma
print("2 + 3 =", 2 + 3)
print("100 - 1 =", 100 - 1)
print("My dog's age in dog years:", 3 * 7)

# Careful! Quotes change everything:
print("2 + 3")    # prints the TEXT 2 + 3 (no math!)
print(2 + 3)      # prints the ANSWER 5 (math!)

# Try it: how many seconds are in one day?
print("Seconds in a day:", 24 * 60 * 60)

