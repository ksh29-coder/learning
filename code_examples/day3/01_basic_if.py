# Another example with a different condition
temperature = 75

if temperature > 70:
    print("It's warm outside! ☀️")

# Comparison operators:
# ==  equals (two equals signs!)
# !=  not equals
# >   greater than
# <   less than
# >=  greater than or equal
# <=  less than or equal

# Try these examples:
number = 5

if number == 5:
    print("The number equals 5")

if number != 3:
    print("The number is NOT 3")

if number > 3:
    print("The number is greater than 3")

if number < 10:
    print("The number is less than 10")

