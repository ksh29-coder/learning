# Day 6 - Creating Lists
# A list is a way to store LOTS of things in one variable, like a backpack
# that can hold many items, or a row of labeled cubbies.

# Creating a list of fruits (strings)
fruits = ["apple", "banana", "cherry"]
print(fruits)

# Creating a list of numbers
scores = [10, 25, 8, 40]
print(scores)

# A list can even mix different types together
mixed_list = ["Alex", 10, "blue", True]
print(mixed_list)

print()

# len() tells us how many items are in a list
print("Number of fruits:", len(fruits))
print("Number of scores:", len(scores))

print()

# An empty list - we can start with nothing and add to it later!
empty_list = []
print("An empty list looks like this:", empty_list)
print("It has this many items:", len(empty_list))

