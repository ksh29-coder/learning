# Day 2 - Example 4: Interactive Info Card
# Create a personalized info card with user input!

print("=" * 40)
print("   PERSONAL INFO CARD CREATOR")
print("=" * 40)
print()
print("Let's create your info card!")
print("Please answer the questions below:")
print()

# Get information from the user
name = input("What's your name? ")
age = input("How old are you? ")
favorite_color = input("What's your favorite color? ")
favorite_food = input("What's your favorite food? ")
hobby = input("What's your favorite hobby? ")
favorite_animal = input("What's your favorite animal? ")

# Display the info card
print()
print("=" * 40)
print("   YOUR INFO CARD")
print("=" * 40)
print("  Name:", name)
print("  Age:", age)
print("  Favorite Color:", favorite_color)
print("  Favorite Food:", favorite_food)
print("  Hobby:", hobby)
print("  Favorite Animal:", favorite_animal)
print("=" * 40)
print()
print("Awesome! Your info card is complete! 🎉")

