# Day 2 - Example 3: Getting Input from Users
# Make your program interactive!

# The input() function asks the user to type something
# The text in quotes is the question you ask
# What the user types gets stored in the variable

print("Welcome! Let's get to know you!")
print()

# Ask for information
name = input("What's your name? ")
age = input("How old are you? ")
favorite_color = input("What's your favorite color? ")

print()
print("Nice to meet you,", name, "!")
print("You're", age, "years old and love", favorite_color, "!")
print("That's awesome! 🎉")

# Important: When you run this, type your answers in the terminal
# The program will wait for you to type and press Enter!

