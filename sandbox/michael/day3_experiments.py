# Experiment 1: Temperature checker with input and elif
temperature = int(input("What is the temperature? "))

if temperature > 70:
    print("It's warm outside! ☀️")
elif temperature < 50:
    print("It's cold outside! ❄️")
else:
    print("It's a nice day outside! ☀️")

